CREATE INDEX ix_worldcup_item_stats_template ON worldcup_item_stats (template_id);

CREATE TABLE social_accounts (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    user_id bigint NOT NULL,
    provider varchar(255) NOT NULL,
    provider_id varchar(255) NOT NULL,
    provider_attributes jsonb NULL,
    CONSTRAINT social_accounts_provider_check
        CHECK (provider IN (
            'GOOGLE', 'NAVER', 'KAKAO', 'CHZZK', 'SOOP', 'TWITCH', 'DISCORD', 'X', 'APPLE'
        )),
    CONSTRAINT uk_social_accounts_provider_user UNIQUE (provider, provider_id),
    CONSTRAINT fk_social_accounts_user FOREIGN KEY (user_id) REFERENCES users (id)
);

CREATE TABLE reactions (
    id uuid NOT NULL PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    target_type varchar(32) NOT NULL,
    target_id uuid NOT NULL,
    reaction_type varchar(16) NOT NULL,
    user_id bigint NULL,
    guest_ip_hash varchar(64) NULL,
    CONSTRAINT reactions_reaction_type_check
        CHECK (reaction_type IN ('LIKE', 'UPVOTE', 'DOWNVOTE')),
    CONSTRAINT reactions_target_type_check
        CHECK (target_type IN (
            'TIER_TEMPLATE', 'TIER_RESULT', 'WORLDCUP_TEMPLATE', 'WORLDCUP_RESULT',
            'COMMUNITY_POST'
        ))
);

CREATE INDEX ix_reactions_target ON reactions (target_type, target_id);

-- 회원: 타겟당 반응 1행 — API 연타·레이스 시 DB가 중복 차단
CREATE UNIQUE INDEX uk_reactions_member_target
    ON reactions (target_type, target_id, user_id)
    WHERE user_id IS NOT NULL;

-- 비회원: IP 해시당 반응 1행 (user_id IS NULL 행만)
CREATE UNIQUE INDEX uk_reactions_guest_target
    ON reactions (target_type, target_id, guest_ip_hash)
    WHERE user_id IS NULL AND guest_ip_hash IS NOT NULL;

CREATE TABLE comments (
    id uuid NOT NULL PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    target_type varchar(32) NOT NULL,
    target_id uuid NOT NULL,
    user_id bigint NULL,
    parent_comment_id uuid NULL,
    body text NOT NULL,
    comment_status varchar(16) NOT NULL,
    author_name varchar(64) NULL,
    author_ip_prefix varchar(45) NULL,
    guest_password varchar(255) NULL,
    guest_ip_hash varchar(64) NULL,
    CONSTRAINT comments_comment_status_check
        CHECK (comment_status IN ('ACTIVE', 'DELETED')),
    CONSTRAINT comments_target_type_check
        CHECK (target_type IN (
            'TIER_TEMPLATE', 'TIER_RESULT', 'WORLDCUP_TEMPLATE', 'WORLDCUP_RESULT',
            'COMMUNITY_POST'
        )),
    CONSTRAINT fk_comments_parent FOREIGN KEY (parent_comment_id) REFERENCES comments (id)
);

CREATE INDEX ix_comments_target ON comments (target_type, target_id);
CREATE INDEX ix_comments_parent ON comments (parent_comment_id);

CREATE TABLE community_posts (
    id uuid NOT NULL PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    title varchar(200) NOT NULL,
    content_html text NOT NULL,
    author_id bigint NULL,
    guest_nickname varchar(64) NULL,
    guest_password_hash varchar(255) NULL,
    guest_ip_hash varchar(64) NULL,
    guest_ip_prefix varchar(45) NULL,
    view_count bigint NOT NULL DEFAULT 0,
    comment_count bigint NOT NULL DEFAULT 0,
    status varchar(20) NOT NULL DEFAULT 'ACTIVE',
    CONSTRAINT ck_community_posts_author_or_guest
        CHECK (
            (author_id IS NOT NULL AND guest_nickname IS NULL AND guest_password_hash IS NULL AND guest_ip_hash IS NULL)
            OR
            (author_id IS NULL AND guest_nickname IS NOT NULL AND guest_password_hash IS NOT NULL
             AND guest_ip_hash IS NOT NULL AND guest_ip_prefix IS NOT NULL)
        ),
    CONSTRAINT community_posts_status_check CHECK (status IN ('ACTIVE', 'DELETED'))
);

CREATE INDEX ix_community_posts_status_created_at ON community_posts (status, created_at DESC);
CREATE INDEX ix_community_posts_author_id ON community_posts (author_id);
