-- Pickty V2 초기 스키마 (신규 환경 전용 — 데이터 이관 없음)
-- PostgreSQL

CREATE TABLE users (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    email varchar(255) NULL,
    nickname varchar(255) NOT NULL,
    profile_image_url varchar(255) NULL,
    display_avatar_url varchar(255) NULL,
    user_name varchar(255) NULL,
    account_status varchar(32) NOT NULL DEFAULT 'ACTIVE',
    birth_year integer NULL,
    gender varchar(16) NULL,
    onboarding_completed_at timestamp(6) NULL,
    role varchar(255) NOT NULL,
    merged_into_user_id bigint NULL,
    CONSTRAINT users_account_status_check
        CHECK (account_status IN ('PENDING', 'ACTIVE', 'DELETED', 'MERGED')),
    CONSTRAINT users_gender_check
        CHECK (gender IS NULL OR gender IN ('MALE', 'FEMALE', 'OTHER')),
    CONSTRAINT users_role_check
        CHECK (role IN ('USER', 'ADMIN')),
    CONSTRAINT uk_users_email UNIQUE (email),
    CONSTRAINT fk_users_merged_into FOREIGN KEY (merged_into_user_id) REFERENCES users (id)
);

CREATE TABLE tier_templates (
    id uuid NOT NULL PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    title varchar(100) NOT NULL,
    description text NULL,
    items jsonb NOT NULL,
    version integer NOT NULL,
    parent_template_id uuid NULL,
    creator_id bigint NULL,
    board_config jsonb NULL,
    thumbnail_url varchar(2048) NULL,
    template_status varchar(20) NOT NULL DEFAULT 'ACTIVE',
    like_count bigint NOT NULL DEFAULT 0,
    comment_count bigint NOT NULL DEFAULT 0,
    view_count bigint NOT NULL DEFAULT 0,
    CONSTRAINT tier_templates_status_check
        CHECK (template_status IN ('ACTIVE', 'DELETED')),
    CONSTRAINT fk_tier_templates_parent FOREIGN KEY (parent_template_id) REFERENCES tier_templates (id)
);

CREATE INDEX ix_tier_templates_creator ON tier_templates (creator_id);
CREATE INDEX ix_tier_templates_status_created ON tier_templates (template_status, created_at DESC);

CREATE TABLE tier_results (
    id uuid NOT NULL PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    template_id uuid NOT NULL,
    user_id bigint NULL,
    snapshot_data jsonb NOT NULL,
    is_public boolean NOT NULL,
    is_temporary boolean NOT NULL,
    result_status varchar(20) NOT NULL DEFAULT 'ACTIVE',
    list_title varchar(100) NULL,
    list_description text NULL,
    thumbnail_url text NULL,
    up_count bigint NOT NULL DEFAULT 0,
    down_count bigint NOT NULL DEFAULT 0,
    comment_count bigint NOT NULL DEFAULT 0,
    view_count bigint NOT NULL DEFAULT 0,
    CONSTRAINT tier_results_result_status_check
        CHECK (result_status IN ('ACTIVE', 'DELETED')),
    CONSTRAINT fk_tier_results_template FOREIGN KEY (template_id) REFERENCES tier_templates (id)
);

CREATE INDEX ix_tier_results_template ON tier_results (template_id);
CREATE INDEX ix_tier_results_user ON tier_results (user_id);

CREATE TABLE worldcup_templates (
