    description text NULL,
    items jsonb NOT NULL,
    layout_mode varchar(32) NOT NULL DEFAULT 'split_diagonal',
    version integer NOT NULL,
    creator_id bigint NULL,
    thumbnail_url varchar(2048) NULL,
    template_status varchar(20) NOT NULL DEFAULT 'ACTIVE',
    like_count bigint NOT NULL DEFAULT 0,
    comment_count bigint NOT NULL DEFAULT 0,
    view_count bigint NOT NULL DEFAULT 0,
    CONSTRAINT worldcup_templates_template_status_check
        CHECK (template_status IN ('ACTIVE', 'DELETED'))
);

CREATE INDEX ix_worldcup_templates_creator ON worldcup_templates (creator_id);
CREATE INDEX ix_worldcup_templates_status_created ON worldcup_templates (template_status, created_at DESC);

CREATE TABLE worldcup_item_stats (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp(6) NOT NULL,
    updated_at timestamp(6) NOT NULL,
    template_id uuid NOT NULL,
    item_id bigint NOT NULL,
    match_count bigint NOT NULL DEFAULT 0,
    win_count bigint NOT NULL DEFAULT 0,
    rerolled_count bigint NOT NULL DEFAULT 0,
    dropped_count bigint NOT NULL DEFAULT 0,
    kept_both_count bigint NOT NULL DEFAULT 0,
    final_win_count bigint NOT NULL DEFAULT 0,
